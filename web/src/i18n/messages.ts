const TABLES: Record<Locale, Dict> = { vi, en }

export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const table = TABLES[locale] ?? vi
  let text = table[key] ?? TABLES.vi[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, String(v))
    }
  }
  return text
}
